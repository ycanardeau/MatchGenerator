using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;

namespace Aigamo.MatchGenerator.Tests;

public class EnumMatchGeneratorTests
{
	private static GeneratorDriverRunResult Run(string source)
	{
		var syntaxTree = CSharpSyntaxTree.ParseText(source);

		var compilation = CSharpCompilation.Create(
			"Tests",
			[syntaxTree],
			[
				MetadataReference.CreateFromFile(typeof(object).Assembly.Location),
				MetadataReference.CreateFromFile(typeof(Func<>).Assembly.Location),
			],
			new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary)
		);

		var generator = new SourceGenerator();

		GeneratorDriver driver = CSharpGeneratorDriver.Create(generator);
		driver = driver.RunGenerators(compilation);

		return driver.GetRunResult();
	}

	private static string RunGenerator(string source)
	{
		var result = Run(source);
		return result.GeneratedTrees[0].GetText().ToString();
	}

	private static List<string> RunGeneratorAll(string source)
	{
		var result = Run(source);
		return [.. result.GeneratedTrees.Select(t => t.GetText().ToString())];
	}

	[Fact]
	public void Generates_Match_Method_For_Enum()
	{
		// Arrange
		var source = @"
namespace Test;

public enum Gender
{
	Male = 1,
	Female,
}
";

		var actual = RunGenerator(source);

		var expected = """
			// <auto-generated />
			namespace Test;

			public static class GenderMatchExtensions
			{
				public static U Match<U>(
					this Gender value,
					Func<U> onMale,
					Func<U> onFemale
				)
				{
					return value switch
					{
						Gender.Male => onMale(),
						Gender.Female => onFemale(),
						_ => throw new System.Diagnostics.UnreachableException(),
					};
				}
			}

			""";

		// Assert
		Assert.Equal(expected, actual);
	}

